package com.scs.beans;

import java.sql.Date;

public class Lab {
	private int eno;
	private String cname;
	private String place;
	private Date etime;
	private int cno;
	public int getEno() {
		return eno;
	}
	public void setEno(int eno1) {
		eno = eno1;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname1) {
		cname = cname1;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place1) {
		place = place1;
	}
	public Date getEtime() {
		return etime;
	}
	public void setEtime(Date etime1) {
		etime = etime1;
	}
	public int getCno() {
		return cno;
	}
	public void setCno(int cno1) {
		cno = cno1;
	}
	
}
